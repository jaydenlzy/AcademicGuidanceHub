    private void populateCompletedReportsTable() {
        List<CompletedReport> completedReports = lc.getCompletedReports();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        for (CompletedReport report : completedReports) {
            model.addRow(new Object[]{
                report.getStudentName(),
                report.getProjectName(),
                report.getFirstMarkerMark(),
                report.getSecondMarkerMark(),
                report.getFinalMark()
            });
        }
    }

